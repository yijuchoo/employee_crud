public class Department {
    private int deptNo;
    private String deptName;

    public Department(int deptNo, String deptName) {
        this.deptNo = deptNo;
        this.deptName = deptName;
    }

    public int getDeptNo() {
        return deptNo;
    }

    public String getDeptName() {
        return deptName;
    }
}
